const color = require('colors/safe')


console.log(color.green('hello')); // outputs green text
console.log(color.red.underline('i like cake and pies')) // outputs red underlined text
console.log(color.inverse('inverse the color')); // inverses the color
console.log(color.rainbow('OMG Rainbows!')); // rainbow
console.log(color.trap('Run the trap')); // Drops the bass